window.findTeam = function(teamId) {
  for (const cat of Object.values(window.TEAMS)) {
    const t = cat.find(t => t.id === teamId);
    if (t) return t;
  }
  return null;
};

window.fallbackLogo = function(img) {
  const wrap = img.parentElement;
  if (!wrap) return;
  const color = img.dataset.color || '#94a3b8';
  const initials = img.dataset.initials || '⚽';
  const size = parseInt(img.dataset.size || '48', 10);
  const div = document.createElement('div');
  div.className = 'team-logo team-logo-badge';
  div.style.width = size + 'px';
  div.style.height = size + 'px';
  div.style.background = color;
  div.style.fontSize = Math.floor(size * 0.34) + 'px';
  div.textContent = initials;
  img.replaceWith(div);
};

window.renderTeamLogo = function(team, size) {
  size = size || 48;
  if (!team) {
    return `<div class="team-logo team-logo-badge" style="width:${size}px;height:${size}px;background:#94a3b8;font-size:${Math.floor(size*0.4)}px">⚽</div>`;
  }
  const initials = team.initials || team.name.charAt(0);
  if (team.logoUrl) {
    return `<img src="${team.logoUrl}" alt="${team.name}" class="team-logo team-logo-img" data-color="${team.color}" data-initials="${initials}" data-size="${size}" style="width:${size}px;height:${size}px;" onerror="window.fallbackLogo(this)" />`;
  }
  return `<div class="team-logo team-logo-badge" style="width:${size}px;height:${size}px;background:${team.color};font-size:${Math.floor(size*0.34)}px">${initials}</div>`;
};

